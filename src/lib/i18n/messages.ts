export const messages = hkMessages;

export type MessageTree = (typeof hkMessages)["zh"];

function getLeaf(obj: unknown, path: string[]): string | undefined {
  if (!obj || typeof obj !== "object") return undefined;
  const [head, ...rest] = path;
  if (!head) return undefined;
  const v = (obj as Record<string, unknown>)[head];
  if (rest.length === 0) return typeof v === "string" ? v : undefined;
  return getLeaf(v, rest);
}

export function translate(
  tree: MessageTree,
  key: string,
  vars?: Record<string, string | number>
): string {
  const path = key.split(".");
  let s = getLeaf(tree, path);
  if (s === undefined) s = key;
  if (vars) {
    for (const [k, val] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, String(val));
    }
  }
  return s;
}
